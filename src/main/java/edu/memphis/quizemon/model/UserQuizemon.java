package edu.memphis.quizemon.model;

public class UserQuizemon {
	private String user_name;
	private String quizemon_name;
	
	UserQuizemon (String user_name, String quizemon_name)
	{
		this.setUser(user_name);
		this.setQuizemon(quizemon_name);
	}
	
	public void setUser(String user_name)
	{
		this.user_name = user_name;
	}
	
	public void setQuizemon(String quizemon_name)
	{
		this.quizemon_name = quizemon_name;
	}
	
	public String getUser()
	{
		return this.user_name;
	}
	
	public String getQuizemon()
	{
		return this.quizemon_name;
	}
}
